// Init Array
let arrOfSomething = [1,'a',2,'b',3,'c']
console.log(arrOfSomething)

// Accessing data on array

// Printing data on index 3 of an array
console.log(arrOfSomething[3])

// Adding new element on the end of an array
arrOfSomething.push(4)
console.log(arrOfSomething)
arrOfSomething.push('d')
console.log(arrOfSomething)

// Adding new element on the start of an array
arrOfSomething.unshift(0)
console.log(arrOfSomething)

// Deleting an element from the end of an array
arrOfSomething.pop()
console.log(arrOfSomething)

// Deleting an element from the start of an array
arrOfSomething.shift()
console.log(arrOfSomething)
